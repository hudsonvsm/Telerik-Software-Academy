using System;


class Assign_Variable_To_Float_Or_Double
{
    static void Main()
    {
        double a = 34.567839023D;
        Console.WriteLine(a);
        float b = 12.345F;
        Console.WriteLine(b);
        double c = 8923.1234857D;
        Console.WriteLine(c);
        float d = 3456.091F;
        Console.WriteLine(d);
    }
}